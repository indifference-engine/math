#ifndef SINE_H

#define SINE_H

/**
 * Calculates the sine of a given angle.
 * @param radians The angle to calculate the sine of, in radians.  Behavior is
 *                undefined if NaN, infinity, negative infinity or unable to be
 *                cast to int.
 * @return The sine of the given angle.
 */
float sine(const float radians);

#endif

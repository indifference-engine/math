#ifndef COSINE_H

#define COSINE_H

/**
 * Calculates the cosine of a given angle.
 * @param radians The angle to calculate the cosine of, in radians.  Behavior is
 *                undefined if NaN, infinity, negative infinity or unable to be
 *                cast to int.
 * @return The cosine of the given angle.
 */
float cosine(const float radians);

#endif

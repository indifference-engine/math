#ifndef MAX_H

#define MAX_H

/**
 * Determines the greater of two floats.
 * @param a The first float to compare.  Behavior is undefined if NaN.
 * @param b The second float to compare.  Behavior is undefined if NaN.
 * @return The greater of the two given floats.
 */
float max(const float a, const float b);

#endif

#ifndef MIN_H

#define MIN_H

/**
 * Determines the lesser of two floats.
 * @param a The first float to compare.  Behavior is undefined if NaN.
 * @param b The second float to compare.  Behavior is undefined if NaN.
 * @return The lesser of the two given floats.
 */
float min(const float a, const float b);

#endif
